package com.module1;

public class Company {

	private int eid;
	private String ename;
	private String edept;
	private int wk_days;
	private float hm_all;
	private float tr_all;
	private float pf;
	private float tax;
	
	
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public String getEdept() {
		return edept;
	}
	public void setEdept(String edept) {
		this.edept = edept;
	}
	public int getWk_days() {
		return wk_days;
	}
	public void setWk_days(int wk_days) {
		this.wk_days = wk_days;
	}
	public float getHm_all() {
		return hm_all;
	}
	public void setHm_all(float hm_all) {
		this.hm_all = hm_all;
	}
	public float getTr_all() {
		return tr_all;
	}
	public void setTr_all(float tr_all) {
		this.tr_all = tr_all;
	}
	public float getPf() {
		return pf;
	}
	public void setPf(float pf) {
		this.pf = pf;
	}
	public float getTax() {
		return tax;
	}
	public void setTax(float tax) {
		this.tax = tax;
	}
	
	
	
}
